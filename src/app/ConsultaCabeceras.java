    private void cargarTablaPorFecha(String fecha) {
        java.util.List<model.CabeceraTransaccion> lista =
                data.CabeceraArchivo.cargarPorFecha(fecha);
        construirModeloTabla(lista);
    }

    private void cargarTablaPorRango(String desde, String hasta) {
        java.util.List<model.CabeceraTransaccion> lista =
                data.CabeceraArchivo.cargarPorRango(desde, hasta);
        construirModeloTabla(lista);
    }

    private void cargarTablaPorTipo(int tipoDoc) {
        java.util.List<model.CabeceraTransaccion> lista =
                data.CabeceraArchivo.cargarPorTipo(tipoDoc);
        construirModeloTabla(lista);
    }

    /**
     * Construye el modelo de la tabla con la lista recibida
     */
    private void construirModeloTabla(java.util.List<model.CabeceraTransaccion> lista) {
        String[] columnas = {
            "Nro Doc", "Fecha", "Tipo", "Descripci贸n",
            "Hecho por", "Monto", "Fecha Actualizaci贸n", "Status"
        };

        javax.swing.table.DefaultTableModel modelo =
                new javax.swing.table.DefaultTableModel(columnas, 0) {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        return false; // solo lectura
                    }
                };

        for (model.CabeceraTransaccion c : lista) {

            String tipoTxt;
            switch (c.getTipoDocu()) {
                case 1 -> tipoTxt = "Factura";
                case 2 -> tipoTxt = "Nota D茅bito";
                case 3 -> tipoTxt = "Nota Cr茅dito";
                default -> tipoTxt = String.valueOf(c.getTipoDocu());
            }

            Object[] fila = {
                c.getNroDocu(),
                c.getFechaDocu(),
                tipoTxt,
                c.getDescripcion(),
                c.getHechoPor(),
                c.getMontoTransaccion(),
                c.getFechaActualizacion(),
                c.isStatusActualizacion() ? "Actualizado" : "Pendiente"
            };
            modelo.addRow(fila);
        }

        tblCabeceras.setModel(modelo);
    }



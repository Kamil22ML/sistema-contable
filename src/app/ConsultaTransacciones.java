    private void cargarTablaTodos() {
        java.util.List<model.DetalleTransaccion> lista =
                data.DetalleArchivo.cargarTodos(); // carga TODO del archivo

        String[] columnas = { "Documento", "Secuencia", "Cuenta", "Débito", "Crédito", "Comentario" };

        javax.swing.table.DefaultTableModel modelo =
                new javax.swing.table.DefaultTableModel(columnas, 0) {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        return false;
                    }
                };

        for (model.DetalleTransaccion d : lista) {
            Object[] fila = {
                    d.getNroDoc(),
                    d.getSecuencia(),
                    d.getCuentaContable(),
                    d.getValorDebito(),
                    d.getValorCredito(),
                    d.getComentario()
            };
            modelo.addRow(fila);
        }

        tblTransacciones.setModel(modelo);
    }

    
    private void cargarTabla(String nroDoc) {
        java.util.List<model.DetalleTransaccion> lista =
                data.DetalleArchivo.cargarPorDocumento(nroDoc);

        String[] columnas = { "Secuencia", "Cuenta", "Débito", "Crédito", "Comentario" };

        javax.swing.table.DefaultTableModel modelo =
                new javax.swing.table.DefaultTableModel(columnas, 0) {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        return false;
                    }
                };

        for (model.DetalleTransaccion d : lista) {
            Object[] fila = {
                    d.getSecuencia(),
                    d.getCuentaContable(),
                    d.getValorDebito(),
                    d.getValorCredito(),
                    d.getComentario()
            };
            modelo.addRow(fila);
        }

        tblTransacciones.setModel(modelo);
    }



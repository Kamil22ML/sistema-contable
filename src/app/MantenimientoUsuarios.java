    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        int fila = tblUsuarios.getSelectedRow();
        if (fila < 0) return;

        String login = String.valueOf(tblUsuarios.getValueAt(fila, 0));

        model.Usuario u = data.UsuarioArchivo.buscarPorLogin(login);
        if (u == null) return;

        txtLogin.setText(u.getLogin());
        txtPassword.setText(u.getPassword()); // si no tienes getter, dime y lo ajusto

        // Tipo
        if ("A".equalsIgnoreCase(u.getTipo())) {
            cbTipo.setSelectedItem("Administrador");
        } else {
            cbTipo.setSelectedItem("Normal");
        }

        txtNombre.setText(u.getNombre());
        txtApellidos.setText(u.getApellidos());
        txtEmail.setText(u.getEmail());

        // Recomendado: no permitir cambiar login cuando estamos editando
        txtLogin.setEnabled(false);

    }//GEN-LAST:event_tblUsuariosMouseClicked


    private void cargarTablaUsuarios() {
        java.util.List<model.Usuario> lista = data.UsuarioArchivo.cargarTodos();

        String[] cols = { "Login", "Tipo", "Nombre", "Apellidos", "Email" };

        javax.swing.table.DefaultTableModel modelo =
                new javax.swing.table.DefaultTableModel(cols, 0) {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        return false;
                    }
                };

        for (model.Usuario u : lista) {
            String tipoTxt = "N";
            if (u.getTipo() != null && u.getTipo().equalsIgnoreCase("A")) {
                tipoTxt = "A";
            }

            modelo.addRow(new Object[]{
                u.getLogin(),
                tipoTxt,
                u.getNombre(),
                u.getApellidos(),
                u.getEmail()
            });
        }

        tblUsuarios.setModel(modelo);
    }

